function spkg_cmd_init(home)
    if spkg.file_exists("Sharp.lua") then
        print("spkg: Sharp.lua already exists.")
        return false
    end
    local name = spkg.dir_exists("src") and "my-project" or "untitled"
    local src = [[
-- generated by spkg init
return {
  name = "%s",
  version = "0.1.0",
  type = "exe",
  src = { "src/**/*.sp" },
  deps = {},
  link = {}
}
]]
    spkg.write_file("Sharp.lua", string.format(src, name))
    print("spkg: created Sharp.lua")
    return true
end

function spkg_cmd_build(manifest)
    return spkg_build.execute(manifest)
end

function spkg_cmd_run(manifest)
    local ok = spkg_cmd_build(manifest)
    if not ok then return false end
    local name = manifest.name or "main"
    local exe = "build/" .. name
    if spkg.file_exists(exe) then
        local r = spkg.run_cmd("./" .. exe)
        print(r.out)
        return r.ok
    end
    print("spkg: executable not found: " .. exe)
    return false
end

function spkg_cmd_add(manifest, args)
    if #args < 1 then
        print("usage: spkg add <name>")
        return false
    end
    manifest.deps = manifest.deps or {}
    manifest.deps[args[1]] = "*"
    local out = "-- generated by spkg\nreturn " .. spkg_dump_lua(manifest)
    spkg.write_file("Sharp.lua", out)
    print("spkg: added dependency '" .. args[1] .. "'")
    return true
end

function spkg_cmd_update(manifest)
    print("spkg: update not yet implemented")
    return false
end

function spkg_cmd_clean()
    if spkg.dir_exists("build") then
        spkg.run_cmd("rm -rf build")
    end
    if spkg.dir_exists("spkg_packages") then
        spkg.run_cmd("rm -rf spkg_packages")
    end
    print("spkg: cleaned.")
    return true
end

return true